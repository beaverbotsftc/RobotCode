package org.firstinspires.ftc.teamcode.experiments;

import com.qualcomm.hardware.limelightvision.LLResult;
import com.qualcomm.hardware.limelightvision.LLResultTypes;
import com.qualcomm.hardware.limelightvision.LLStatus;
import com.qualcomm.hardware.limelightvision.Limelight3A;
import com.qualcomm.robotcore.eventloop.opmode.Disabled;
import com.qualcomm.robotcore.eventloop.opmode.LinearOpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;

import org.firstinspires.ftc.robotcore.external.navigation.Pose3D;

import java.util.List;
@TeleOp(name = "Limelight Distance Experiment")
public class LimelightDistanceExperiment extends LinearOpMode {

    private Limelight3A limelight;
    private boolean pipeline0 = false;

    @Override
    public void runOpMode() throws InterruptedException
    {
        limelight = hardwareMap.get(Limelight3A.class, "limelight");

        telemetry.setMsTransmissionInterval(11);

        limelight.pipelineSwitch(9);

        limelight.start();

        telemetry.addData(">", "Robot Ready.  Press Play.");
        telemetry.update();
        waitForStart();

        while (opModeIsActive()) {

            if(gamepad1.triangleWasPressed()){
                pipeline0 = true;
                limelight.pipelineSwitch(0);
            }else if (gamepad1.crossWasPressed()){
                pipeline0 = false;
                limelight.pipelineSwitch(9);
            }

            LLStatus status = limelight.getStatus();
            telemetry.addData("Name", "%s",
                    status.getName());
            telemetry.addData("LL", "Temp: %.1fC, FPS: %d",
                    status.getTemp(), (int)status.getFps());

            LLResult result = limelight.getLatestResult();

            if(pipeline0){

                if (result.isValid() && result.getBotpose() != null) {
                    // Access general information
                    Pose3D botpose = result.getBotpose();

                    telemetry.addData("Target Area", result.getTa());
                    telemetry.addLine("");
                    telemetry.addData("Botpose", botpose.toString());
                    telemetry.addLine("");


                    // Access fiducial results
                    List<LLResultTypes.FiducialResult> fiducialResults = result.getFiducialResults();
                    for (LLResultTypes.FiducialResult fr : fiducialResults) {
                        telemetry.addData("Fiducial", "ID: %d, Family: %s, X: %.2f, Y: %.2f", fr.getFiducialId(), fr.getFamily(), fr.getTargetXDegrees(), fr.getTargetYDegrees());
                    }
                } else {
                    telemetry.addData("Limelight", "No data available");
                }
            }else{
                if (result.isValid()) {
                    // Access general information
                    telemetry.addData("Target Area", result.getTa());
                    telemetry.addLine("");

                    // Access fiducial results
                    List<LLResultTypes.FiducialResult> fiducialResults = result.getFiducialResults();
                    for (LLResultTypes.FiducialResult fr : fiducialResults) {
                        telemetry.addData("Fiducial", "ID: %d, Family: %s, X: %.2f, Y: %.2f", fr.getFiducialId(), fr.getFamily(), fr.getTargetXDegrees(), fr.getTargetYDegrees());
                    }
                } else {
                    telemetry.addData("Limelight", "No data available");
                }

            }

            telemetry.update();
        }

        limelight.stop();
    }
}
